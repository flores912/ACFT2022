import jose.flores.acft.ui.items.EventItem
@Composable
fun HomeScreen(navController: NavHostController){

    var events = Repository().getEvents()

    LazyColumn(
            modifier = Modifier
                .fillMaxSize()
                .clickable {
                    navController.navigate("event_details")

                },
            content = {
            items(events){event ->
                EventItem(event = event)
            }
        },
    )

}

